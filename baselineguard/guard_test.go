func setupCompliantRepo(t *testing.T) string {
	t.Helper()
	dir := t.TempDir()

	writeFile(t, dir, "AGENTS.md", "# Test\n\n> Canonical instructions: AGENTS.md\n")
	writeFile(t, dir, "CLAUDE.md", "# Test\n\nThis repo uses [AGENTS.md](AGENTS.md) as the canonical instruction file.\n")
	writeFile(t, dir, "GEMINI.md", "# Test\n\nThis repo uses [AGENTS.md](AGENTS.md) as the canonical instruction file.\n")
	writeFile(t, dir, ".claude/settings.json", "{}\n")
	writeFile(t, dir, ".gemini/settings.json", "{\n  \"context\": {\n    \"fileName\": [\"AGENTS.md\", \"GEMINI.md\"]\n  }\n}\n")
	writeFile(t, dir, ".github/copilot-instructions.md", "See AGENTS.md in the repository root.\n")
	writeFile(t, dir, ".codex/config.toml", `
approval_policy = "on-request"

[profiles.readonly_quiet]
approval_policy = "never"

[profiles.review]
approval_policy = "on-request"

[profiles.workspace_auto]
approval_policy = "on-failure"

[profiles.ci_json]
approval_policy = "never"
`)
	writeFile(t, dir, ".codex/agents/reviewer.toml", `name = "reviewer"`)
	writeFile(t, dir, ".codex/agents/repo_explorer.toml", `name = "repo_explorer"`)

	surface, _ := json.Marshal(map[string]any{
		"version": 1,
		"skills":  []map[string]any{{"name": "test_skill"}},
	})
	writeFile(t, dir, ".agents/skills/surface.yaml", string(surface))
	writeFile(t, dir, ".agents/skills/test_skill/SKILL.md", "---\nname: test_skill\ndescription: \"Core build, test, release, and maintenance workflow for the test repo.\"\nallowed-tools:\n  - Bash\n---\n# Test\n")
	writeFile(t, dir, ".claude/skills/test_skill/SKILL.md", "---\nname: test_skill\ndescription: 'Core build, test, release, and maintenance workflow for the test repo.'\nallowed-tools:\n  - Bash\n---\n\n<!-- GENERATED BY hg-skill-surface-sync.sh FROM .agents/skills/test_skill/SKILL.md; DO NOT EDIT -->\n\nCompatibility mirror of the canonical `test_skill` skill.\n\n\n# Test\n")
	writeFile(t, dir, ".claude/skills/test-skill/SKILL.md", "---\nname: test-skill\ndescription: 'Hyphenated compatibility alias for the test_skill workflow.'\nallowed-tools:\n  - Bash\n---\n\n<!-- GENERATED BY hg-skill-surface-sync.sh FROM .agents/skills/test_skill/SKILL.md; DO NOT EDIT -->\n\nCompatibility alias for the canonical `test_skill` skill.\n\n\n# Test\n")

	return dir
}

func writeFile(t *testing.T, base, name, content string) {
	t.Helper()
	path := filepath.Join(base, name)
	if err := os.MkdirAll(filepath.Dir(path), 0755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(path, []byte(content), 0644); err != nil {
		t.Fatal(err)
	}
}

func TestCheck_CompliantRepo(t *testing.T) {
	dir := setupCompliantRepo(t)
	report := Check(dir)

	if !report.Passed {
		for _, f := range report.Findings {
			if !f.Passed {
				t.Errorf("unexpected failure: %s — %s", f.Check, f.Message)
			}
		}
		t.Fatalf("expected PASS, got %d failures", report.Failed)
	}
}

