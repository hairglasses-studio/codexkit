func TestCheck_MCPSyncHonorsProfilePolicy(t *testing.T) {
	dir := setupCompliantRepo(t)
	writeFile(t, dir, ".mcp.json", `{
  "mcpServers": {
    "demo": {
      "command": "bash",
      "args": ["-lc", "exec ./scripts/run-demo.sh"]
    }
  }
}
`)
	writeFile(t, dir, ".codex/mcp-profile-policy.json", `{
  "version": 1,
  "profiles": [
    {
      "name": "demo_review",
      "from": "demo",
      "comment": "Read-only demo profile."
    }
  ]
}
`)
	writeFile(t, dir, ".codex/config.toml", `
[profiles.readonly_quiet]
approval_policy = "never"

[profiles.review]
approval_policy = "on-request"

[profiles.workspace_auto]
approval_policy = "on-failure"

[profiles.ci_json]
approval_policy = "never"
`)
	if report := mcpsync.Sync(dir, false); len(report.Errors) > 0 {
		t.Fatalf("unexpected mcpsync errors: %v", report.Errors)
	}

	report := Check(dir)
	for _, f := range report.Findings {
		if f.Check == "mcp_sync" && !f.Passed {
			t.Fatalf("expected policy-derived MCP server to pass, got: %s", f.Message)
		}
	}
}
